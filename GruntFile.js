module.exports = function(grunt) {

  // ===========================================================================
  // CONFIGURE GRUNT ===========================================================
  // ===========================================================================
  grunt.initConfig({
    pkg: grunt.file.readJSON('package.json'),
	connect: {
		server: {
			options: {
				port: 5000,
				base: './',
				keepalive: true,
				debug: true
			}
		}
	}

  });

  // ===========================================================================
  // LOAD GRUNT PLUGINS ========================================================
  // ===========================================================================
  grunt.loadNpmTasks('grunt-contrib-connect');

};